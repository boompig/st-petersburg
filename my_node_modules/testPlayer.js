/* eslint-env node */

const c = require("../js/cards.js");
const cards = c.cards;
const Card = c.Card;

/******* Affordability checks ******/
exports.testCostLower = function (test) {
    var card = cards.market;
    test.strictEqual(card.getCost(Card.locations.LOWER_BOARD), card.cost - 1, "reduced cost from lower board");
    test.done();
};
