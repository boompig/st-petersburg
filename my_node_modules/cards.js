function Card (name, cost, coinYield, pointYield) {
    this.name = name;
    this.cost = cost;
    this.pointYield = pointYield;
    this.coinYield = coinYield;
};

Card.locations = {
    HAND: 1,
    UPPER_BOARD: 2,
    LOWER_BOARD: 3,
};

Card.prototype.getCost = function (location) {
    if (location === Card.locations.LOWER_BOARD) {
        return Math.max(this.cost - 1, 1);
    } else {
        return this.cost;
    }
};

// just a list of all the cards, for easy access
var cards = {
    market: new Card("Market", 5, 0, 1),
    customs_house: new Card("Customs House", 8, 0, 2),
    firehouse: new Card("Firehouse", 11, 0, 3),
    hospital: new Card("Hospital", 14, 0, 4),
};

exports.Card = Card;
exports.cards = cards;
